#!/bin/bash

# 
# More
# - Command: more [options] [file]
# - Manual: "Displays the contents of a file in a terminal."
# - Also shows the percentage of viewing progress in the console.
# 

more file.txt

# Once inside:
# - Use the up/down arrows to move one entire screen at a time.
# - Use Enter to advance one line at a time.
# - Exit with "q".


# - Set number of custom lines per screen
more -n 5 file.txt 