#!/bin/bash

# 
# Less
# - Command: less [options] [file]
# - Allows to view the contents of a file one screen at a time.
# 

less file.txt

# - Scroll up/down with arrows keys
# - Scroll up/down one entire page with PageUp, PageDown
# - Move to start/end with Home, End
# - Exit with "q"


# - Display line numbers alongside the text
less -N file.txt